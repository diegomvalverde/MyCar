    public void keyTyped(KeyEvent e) {
        char k = e.getKeyChar();
        if (k == KeyEvent.VK_UP){
            switch (this.motorSystem.gear.gearbox){
                case "1":
                    this.motorSystem.move.acceleration = Constants.accelerationFirst;
                    break;
            }
        }
    }

